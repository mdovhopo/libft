#include "libft.h"
#include <stdio.h>

t_vec2	ft_add_vec2(t_vec2 v1, t_vec2 v2)
{
	t_vec2 res;

	res.x = v1.x + v2.x;
	res.y = v1.y + v2.y;
	return (res);
}
